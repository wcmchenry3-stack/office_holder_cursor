class TestClaudeClientBackoffOn429:
    def test_retries_on_rate_limit(self):
        import anthropic

        from src.services.claude_client import ClaudeClient

        client = ClaudeClient.__new__(ClaudeClient)
        client._client = MagicMock()
        client._model = "claude-sonnet-4-20250514"

        # First call raises RateLimitError, second succeeds
        mock_response = MagicMock()
        mock_response.content = [
            MagicMock(
                text=json.dumps(
                    {
                        "file_path": "src/scraper/table_parser.py",
                        "diff": "-old\n+new\ndef test_x(): pass",
                        "test_code": "",
                        "explanation": "fix",
                    }
                )
            )
        ]
        mock_429 = MagicMock()
        mock_429.status_code = 429
        mock_429.headers = {}
        rate_error = anthropic.RateLimitError(
            message="rate limited",
            response=mock_429,
            body=None,
        )
        client._client.messages.create = MagicMock(side_effect=[rate_error, mock_response])

        with patch("time.sleep"):
            result = client._call_claude_fix("title", "body", "content")
        assert result is not None
        assert client._client.messages.create.call_count == 2


