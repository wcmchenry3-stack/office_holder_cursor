def _make_proposal(
    file_path="src/scraper/table_parser.py",
    diff="--- a/src/scraper/table_parser.py\n+++ b/src/scraper/table_parser.py\n@@ -10,3 +10,5 @@\n-    old_line\n+    new_line\n+    extra_line\n",
    test_code="def test_fix():\n    assert True\n",
    explanation="Fix date parsing for edge case",
):
    return ParserFixProposal(
        file_path=file_path,
        diff=diff,
        test_code=test_code,
        explanation=explanation,
    )


