class TestAutoFixPipeline:
    def test_full_pipeline_creates_pr(self):
        issue = {
            "number": 42,
            "title": "[Parser Bug] Date parse fails on empty string",
            "body": _SAMPLE_ISSUE_BODY,
            "labels": [
                {"name": "parser-bug"},
                {"name": "parse-error:pf-abc123def456"},
            ],
        }

        github = MagicMock()
        github.get_file_content.return_value = {
            "content": "def format_date(s):\n    return s\n",
            "sha": "abc123",
        }
        github.get_default_branch_sha.return_value = "sha456"
        github.create_branch.return_value = {"ref": "refs/heads/fix/parser-auto-pf-abc123def456"}
        github.update_file.return_value = {"content": {}}
        github.create_file.return_value = {"content": {}}
        github.create_pull_request.return_value = {
            "html_url": "https://github.com/repo/pull/99",
            "number": 99,
        }

        claude = MagicMock()
        claude.propose_parser_fix.return_value = _make_proposal()

        result = run_auto_fix_for_issue(issue, github, claude)
        assert result["status"] == "pr_created"
        assert result["pr_url"] == "https://github.com/repo/pull/99"
        github.create_pull_request.assert_called_once()
        pr_args = github.create_pull_request.call_args
        assert pr_args[1]["draft"] is True
        assert pr_args[1]["base"] == "dev"

    def test_skips_issue_without_fingerprint(self):
        issue = {
            "number": 42,
            "title": "test",
            "body": "test",
            "labels": [{"name": "parser-bug"}],
        }
        result = run_auto_fix_for_issue(issue, MagicMock(), MagicMock())
        assert result["status"] == "skipped"

    def test_criteria_failed_does_not_create_pr(self):
        issue = {
            "number": 42,
            "title": "[Parser Bug] test",
            "body": _SAMPLE_ISSUE_BODY.replace("ValueError", "RuntimeError"),
            "labels": [
                {"name": "parser-bug"},
                {"name": "parse-error:pf-abc123def456"},
            ],
        }

        github = MagicMock()
        github.get_file_content.return_value = {"content": "code", "sha": "sha"}

        claude = MagicMock()
        claude.propose_parser_fix.return_value = _make_proposal()

        result = run_auto_fix_for_issue(issue, github, claude)
        assert result["status"] == "criteria_failed"
        github.create_branch.assert_not_called()
